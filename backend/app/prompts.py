from __future__ import annotations

# ---------------------------------------------------------------------------
# Agent A — 文档综述与分组
# ---------------------------------------------------------------------------
DEFAULT_AGENT_A_INSTRUCTION = (
    "1. 先用 2-3 句话概括整篇文档的核心主线（解决什么问题？面向谁？学完能做到什么？），"
    "   避免空泛表达如'本文档讲述了多个主题'。\n"
    "2. 分组应按「教学进度」而非「PDF 物理章节」划分；同一概念跨页的内容应归为同组。\n"
    "3. 每组命名要让学生看到标题就能理解该组在讲什么，例如："
    "   '矩阵乘法与维度匹配'优于 'Section 3'。\n"
    "4. 如果文档有封面/目录/参考文献页，把它们单独归为辅助组而非和正文混在一起。\n"
    "5. knowledge_map 中应标注「概念 A 是概念 B 的前置条件」这类关系，而非仅列名词。\n"
    "6. learning_arc 的 why 字段必须写清楚'为什么必须先学 X 才能理解 Y'。"
)

# ---------------------------------------------------------------------------
# Agent B — 章节总结
# ---------------------------------------------------------------------------
DEFAULT_AGENT_B_INSTRUCTION = (
    "1. summary 要回答三个问题：这组讲什么？为什么学它？学完后学生能做到什么？\n"
    "2. key_concepts 只保留最核心的 3-5 个，每个附一句话白话解释。\n"
    "3. prerequisites 要具体：'需要理解向量内积的几何含义'优于'线性代数基础'。\n"
    "4. misconceptions 要写成「学生常犯的错误 → 正确理解」的对比格式。\n"
    "5. 如果本组与前后组存在强概念承接，在 summary 末尾加一句过渡提示。"
)

# ---------------------------------------------------------------------------
# Agent C — 逐页解释（核心 prompt，对输出质量影响最大）
# ---------------------------------------------------------------------------
DEFAULT_AGENT_C_INSTRUCTION = (
    # —— 内容策略
    "1. 先忠实于原始页面内容，不要添加页面上不存在的信息或例子。\n"
    "2. 用「这页到底在干什么」开头，一句话总结本页核心动作（定义概念/推导公式/给例子/证明定理）。\n"
    "3. 术语第一次出现时必须立刻给白话解释，格式为：「术语 — 一句话白话」。\n"
    "4. 如果页面有步骤推导，用编号列表逐步拆解，每步先写结论再解释为什么。\n"
    # —— 教学质量
    "5. teaching.definition 不要照抄原文，要用学生听得懂的话重新表达。\n"
    "6. teaching.intuition 要回答'为什么是这样'而非仅重复定义；能用类比就用类比。\n"
    "7. teaching.example 优先使用页面上已有的例子进行解读；没有例子时可构造一个最简情形。\n"
    "8. teaching.pitfall 要写成具体错误场景而非泛泛说'注意不要搞混'。\n"
    "8.1 面向 ADHD 学习者：用短句、低负担分段、先结论后细节，避免一次塞入过多新术语。\n"
    # —— 连续链与微任务
    "9. continuity 必须具体：prevBridge 引用上一页的具体结论或公式，"
    "   thisPageNew 点明本页引入了什么新概念，nextPreview 预告下一页会用到本页哪个结论。\n"
    "10. 输出必须是干净 Markdown；数学表达统一使用 $...$（行内）和 $$...$$（块级）。"
)

# ---------------------------------------------------------------------------
# Chat — 页内问答
# ---------------------------------------------------------------------------
DEFAULT_CHAT_INSTRUCTION = (
    "1. 先一句话直接回答问题，再展开解释。\n"
    "1.1 面向 ADHD 学习者：先给结论，再给 2-4 条可执行步骤，避免长段落。\n"
    "2. 优先用当前页的原文证据回答；引用时指出具体在页面哪个位置（如'第3段''公式(2)'）。\n"
    "3. 如果问题涉及以前页面的概念，先说清楚当前页在做什么，再简要回顾前置内容。\n"
    "4. 遇到'我不理解 X'类问题，用类比或最简例子帮助建立直觉，不要重复原文。\n"
    "5. 遇到'X 和 Y 有什么关系'类问题，先列对比表格再给文字说明。\n"
    "6. 回答末尾给一个可立刻执行的下一步建议，如'现在回到页面看公式(3)的右边项'。\n"
    "7. 数学表达使用 $...$，输出保持 Markdown 可渲染。"
)

# ---------------------------------------------------------------------------
# Formula — 公式识别
# ---------------------------------------------------------------------------
DEFAULT_FORMULA_INSTRUCTION = (
    "1. 只返回高度确定的数学公式和方程；自然语言中嵌入的变量名（如 x=3）也应识别。\n"
    "2. 禁止把纯自然语言整句识别为公式。\n"
    "3. 对于复杂公式，保留原始的上下标结构；使用 LaTeX 格式输出。"
)


def default_prompt_config() -> dict[str, str]:
    return {
        "agent_a_instruction": DEFAULT_AGENT_A_INSTRUCTION,
        "agent_b_instruction": DEFAULT_AGENT_B_INSTRUCTION,
        "agent_c_instruction": DEFAULT_AGENT_C_INSTRUCTION,
        "chat_instruction": DEFAULT_CHAT_INSTRUCTION,
        "formula_instruction": DEFAULT_FORMULA_INSTRUCTION,
    }
